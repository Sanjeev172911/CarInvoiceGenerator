public class Invoice {
    int ridesCnt;
    double totalFare;
    double averageFarePerRide;

    public Invoice(int ridesCnt, double totalFare, double averageFarePerRide) {
        this.ridesCnt = ridesCnt;
        this.totalFare = totalFare;
        this.averageFarePerRide = averageFarePerRide;
    }

}
